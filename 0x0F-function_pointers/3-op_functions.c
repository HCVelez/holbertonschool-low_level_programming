#include "3-calc.h"

/**
 * op_add - function to add 2 ints
 * @a: 1st int
 * @b: 2nd int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract 2 ints
 * @a: 1st int
 * @b: 2nd int
 * Return: answer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply 2 ints
 * @a: 1st int
 * @b: 2nd int
 * Return: solution
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide 2 ints
 * @a: 1st int
 * @b: 2nd int
 * Return: solution
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to mod 2 ints.
 * @a: 1st int
 * @b: 2nd int
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

